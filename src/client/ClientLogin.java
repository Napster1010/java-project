    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try
        {
            String ip = txtIP.getText();
            int port = Integer.parseInt(txtPort.getText());
            String password = new String(txtPass.getPassword());
                        
            
            Socket socket = new Socket(ip, port);            
            DataInputStream dis = new DataInputStream(socket.getInputStream());//InputStream
            DataOutputStream dos = new DataOutputStream(socket.getOutputStream());//OutputStream  
            
            //Send the password to server for authentication and get the response
            dos.writeUTF(password);
            System.out.println("Client sent " + password);
            String serverResponse = dis.readUTF();
            System.out.println("Server responded " +serverResponse);
            if(serverResponse.equals("Login successful"))
            {
                JOptionPane.showMessageDialog(rootPane, "Login Successful. Welcome to Y-NOT Text Editor", "Y-NOT Editor Client Login", JOptionPane.INFORMATION_MESSAGE);
                new ClientMain().setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Login Failed. Please check your Password and try again.", "Y-NOT Editor Client Login", JOptionPane.ERROR_MESSAGE);                
                dis.close();
                dos.close();
                socket.close();
            }
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Couldn't connect to the server. Invalid details entered or the server is in a bad state", "Y-NOT Editor Client Login", JOptionPane.ERROR_MESSAGE);            
            txtIP.setText("");
            txtPort.setText("");
            txtPass.setText("");
            txtIP.requestFocus();
            e.printStackTrace();            
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

